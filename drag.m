function a = drag(v, r)
rEarth = 6378.1363;
omegaEarth = [0, 0, 7.2921158553E-5];
altT = [0; 25; 30; 40; 50; 60; 70; 80; 90; 100; 110; 120; 130; 140; 150; 180; 200; 250; 300; 350; 400; 450; 500; 600; 700; 800; 900; 1000];
rho0T = (1/(1E-9))*[1.225; 3.899E-2; 1.774E-2; 3.972E-3; 1.057E-3; 3.206E-4; 8.77E-5; 1.905E-5; 3.396E-6; 5.297E-7; 9.661E-8; 2.438E-8; 8.484E-9; 3.845E-9; 2.070E-9; 5.464E-10; 2.789E-10; 7.248E-11; 2.418E-11; 9.518E-12; 3.725E-12; 1.585E-12; 6.967E-13; 1.454E-13; 3.614E-14; 1.170E-14; 5.245E-15; 3.019E-15];
scaleHT = [7.249; 6.349; 6.682; 7.554; 8.382; 7.714; 6.549; 5.799; 5.382; 5.877; 7.263; 9.473; 12.636; 16.149; 22.523; 29.740; 37.105; 45.546; 53.628; 53.298; 58.515; 60.828; 63.822; 71.835; 88.667; 124.64; 181.05; 268];

CD =2;
aom = .01*1E-6;

h = norm(r) - rEarth;


EAM = [altT, rho0T, scaleHT];
for ii=1:length(EAM)
   if h < EAM(ii,1)
       h0 = EAM(ii-1,1);
       H = EAM(ii-1,3);
       rho0 = EAM(ii-1,2);
       break;
   end
   if ii == length(EAM)
       h0 = EAM(ii,1);
       H = EAM(ii,3);
       rho0 = EAM(ii,2);
   end
end
rho = rho0*exp(-(h-h0)/H);

vrel = v - cross(omegaEarth,r);

a = -.5*CD*aom*rho*norm(vrel)*vrel;
end